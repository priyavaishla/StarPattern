package starpatterns;

public class example4 {

	public static void main(String[] args) {
                for(int x=1;x<=5;x++){
				for(int j=1;j<=x;j++){
					System.out.print(" ");
					}
			    for(int i=5;i>=x;i--){
			     System.out.print("*");
		            }
                 System.out.println("\n");
		            }

}
}